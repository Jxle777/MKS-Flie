// 添加新文件的辅助函数
window.addNewFile = function(directoryPath, fileName, fileType, fileSize, filePath) {
    if (!window.dynamicFileConfig[directoryPath]) {
        window.dynamicFileConfig[directoryPath] = [];
    }
    
    const newFile = {
        name: fileName,
        type: fileType, // 'file' 或 'folder'
        size: fileSize,
        modified: new Date().toLocaleDateString('zh-CN') + ' ' + new Date().toLocaleTimeString('zh-CN', {hour: '2-digit', minute: '2-digit'}),
        path: filePath
    };
    
    window.dynamicFileConfig[directoryPath].push(newFile);
    console.log(`已添加新文件: ${fileName} 到目录: ${directoryPath}`);
};

